<p><a name="reg"><b>Le r&eacute;glement int&eacute;rieur du club</b></a>
<menu>
 Le but du pr&eacute;sent r&egrave;glement est de faciliter l'utilisation des courts et faire appliquer certaines r&egrave;gles sportives de biens&eacute;ance. Si l'auto discipline n'est pas suffisante, les membres du bureau sont habilit&eacute;s &agrave; le faire respecter.<br> 
<b>Article 1 : Les abonn&eacute;s</b><br>
TOUT JOUEUR DOIT ETRE ABONNE du TC........
Sont consid&eacute;r&eacute;s comme abonn&eacute;s du club les personnes ayant r&eacute;gl&eacute; leur cotisation annuelle. L'ann&eacute;e tennistique commence le 1er Octobre et se termine le 31 Septembre de l'ann&eacute;e suivante.
L'abonn&eacute; ne doit pas pr&ecirc;ter la carte d'entr&eacute;e perfor&eacute;e, ou r&eacute;server un cr&eacute;neau horaire pour une personne ext&eacute;rieure au club (famille, amis...), sous peine de voir son compte de r&eacute;servation d&eacute;sactiv&eacute; et sa carte retir&eacute;e. En cas de blessure, le club ne pourra &ecirc;tre tenu pour responsable si une personne ext&eacute;rieure joue sur ses installations sans y &ecirc;tre autoris&eacute;e.
Tous les abonn&eacute;s ont les m&ecirc;mes droits et les m&ecirc;mes devoirs sur tous les courts. Ils sont responsables de l'&eacute;tat dans lequel ils laissent le terrain apr&egrave;s y avoir jou&eacute; (ramassage des bouteille)<br>
<b>Article 2 : L'&eacute;cole de tennis</b><br>
Avant de d&eacute;poser leurs enfants au club, les parents doivent s'assurer qu'il y a bien un moniteur/responsable pour les accueillir. Les parents sont pri&eacute;s de reprendre leurs enfants &agrave; la fin des entrainements.<br>
<b>Article 3 : Les r&eacute;servations de court</b><br>
Les r&eacute;servations se font sur le site : http://.......  ou au club avec le PC mis &agrave; votre disposition.
Il est interdit de r&eacute;server deux heures cons&eacute;cutives avec la m&ecirc;me personne !
La r&eacute;servation d'un cr&eacute;neau sur un court, rend impossible une deuxi&egrave;me r&eacute;servation sur ce m&ecirc;me court. Lorsque l'horaire du cr&eacute;neau r&eacute;serv&eacute; est d&eacute;pass&eacute;, vous pouvez &agrave; nouveau r&eacute;server pour une autre heure, non cons&eacute;cutive &agrave; la pr&eacute;c&eacute;dente ou un autre jour. Tout abonn&eacute; doit r&eacute;server un court avec un autre abonn&eacute; du club pour que sa r&eacute;servation soit valid&eacute;e.
Un court peut &ecirc;tre utilis&eacute; pendant une heure, soit en simple soit en double, &agrave; condition que les joueurs occupant le court soient tous abonn&eacute;s du TC.
Exceptionnellement un court peut &ecirc;tre r&eacute;serv&eacute; par un abonn&eacute; avec un, deux ou trois invit&eacute;s sous r&eacute;serve d'avoir pr&eacute;venu le bureau par mail club@freefree.fr ou, lors des permanences, le mercredi 18-19h, samedi 10-12h pr&eacute;c&eacute;dant sa r&eacute;servation.
Le tarif est de 3euros par invit&eacute;. ( ex : 1 abonn&eacute; + 3 invit&eacute;s = 9 euros l'heure).<br>
<b>Article 4 : Les vols</b><br>
Le club d&eacute;cline toute responsabilit&eacute; en cas de perte ou de vol sur les courts, dans le club-house et les espaces environnants.<br>
<b>Article 5 : Les priorit&eacute;s d'acc&egrave;s</b><br>
Des priorit&eacute;s sont instaur&eacute;es sur certains courts pour  l'&eacute;cole de tennis, les matchs en comp&eacute;tition et les entrainements &eacute;quipes.<br>
<b>Article 6 : R&egrave;gles diverses</b><br>
Quel que soit le temps la tenue de tennis est obligatoire. Sur les courts, les chaussures de tennis ne doivent pas avoir de semelles noires. Sur le court couvert il est demand&eacute; aux adh&eacute;rents de veiller &agrave; la fermeture des baies coulissantes et &agrave; l'extinction des lumi&egrave;res. Les bicyclettes et motocyclettes doivent &ecirc;tre rang&eacute;es dans les emplacements pr&eacute;vus &agrave; cet effet
Boites, bouteilles, papiers etc. doivent &ecirc;tre d&eacute;pos&eacute;s dans les poubelles pr&eacute;vues &agrave; cet effet ou rapport&eacute;es &agrave; domicile.
Les diverses installations doivent &ecirc;tre respect&eacute;es.<br>
<b>Article 7 : Les sanctions</b><br>
Tout manquement au pr&eacute;sent r&egrave;glement pourra donner lieu &agrave; des sanctions d&eacute;cid&eacute;es par les membres du bureau.
Tout manquement aux r&egrave;gles de r&eacute;servation entraine sa nullit&eacute;.
Toute personne &eacute;trang&egrave;re au T.C. surprise &agrave; jouer sur les installations devra s'acquitter du r&egrave;glement de 10 euros pour toute heure jou&eacute;e. 
