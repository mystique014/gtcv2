<p><a name="fon"><b>Les fonctionnalit&eacute;s du syst&egrave;me :</b></a>
<menu>
 Ce syst&egrave;me de r&eacute;servation permet :
<br> - aux adh&eacute;rents de r&eacute;server leur cr&eacute;neau de tennis.
<br> - aux responsables de g&eacute;rer compl&eacute;tement un club de tennis y compris la comptabilit&eacute;.
<br> Voyons en d&eacute;tail les possibilit&eacute;s : 
<br><br><b> 1 - Pour l'adh&eacute;rent :</b>
<br><br><b>R&eacute;server :</b> 
<br>Il poss&egrave;de un identifiant et mot de passe fournis par les responsables du club.
	<br>L'adh&eacute;rent peut r&eacute;server une heure de tennis sur le court de son choix avec un membre du club (qui recevra un email de confirmation) ou un invit&eacute;.
	<br>S'il choisit de r&eacute;server avec un invit&eacute;, il aura pris soin auparavant de r&eacute;gler des "ticket invit&eacute;".
	Le principe est simple les responsables saisissent le nombre d'heures invit&eacute;s r&eacute;gl&eacute; par l'abonn&eacute;.
	D&egrave;s que l'abonn&eacute; r&eacute;serve avec un invit&eacute;, son compteur est d&eacute;cr&eacute;ment&eacute; automatiquement.
<br><br><b> G&eacute;rer son compte :</b> 
<br>L'adh&eacute;rent peut changer son mot de passe et renseigner son adresse mail, il peut changer l'aspect visuel du site.
<br><br><b> D&eacute;poser une annonce : </b>
<br>L'adh&eacute;rent qui le souhaite peut consulter les annonces et d&eacute;poser sa propre annonce pour indiquer ses disponibilit&eacute;s.
<br><br><b>2 - Pour les responsables :</b>
<br><br><b> G&eacute;rer les adh&eacute;rents :</b> 
<br>( identifiant, mot de passe, photo, nom, pr&eacute;nom, date de naissance, email, t&eacute;l, adrese, type d'abonnement, compteur invit&eacute;...)
Visualiser les statistiques de r&eacute;servation, comparer les abonnements sur plusieurs ann&eacute;es.
Envoyer des emails avec pi&egrave;ces jointes par un simple clic aux adh&eacute;rents.
<br><br><b> G&eacute;rer la comptabilit&eacute; du club:</b> 
<br>Gestion par ann&eacute;e sportive des abonn&eacute;s etdes frais de fonctionnement du club (salaires, charges etc...)
<br>Edition de bilan comptable
<br>Gestion de la tresorerie
<br><br><b> G&eacute;rer l'aspect du site :</b> 
<br>Configurer l'aspect du site (couleur, langue, menu)
Configurer les courts.
D&eacute;poser des annonces en page d'accueil.
<br><br><b> Effectuer/Retirer des r&eacute;servations :</b> 
<br>Les responsables peuvent effectuer des r&eacute;servations sur plusieurs heures pour des activit&eacute;s diff&eacute;rentes et param&eacute;trables(Ecole de tennis, Entra&icirc;nement, ...) avec des p&eacute;riodicit&eacute;s (Chaque jour, chaque semaine etc ...)
Les responsables peuvent retirer des r&eacute;servations d'adh&eacute;rents si n&eacute;cessaire.
